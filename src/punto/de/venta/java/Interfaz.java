    private void calculaTotal(){
        int aux = 0;
        int col = tabla.getRowCount();
        for (int i = 0; i < col; i++) {
            aux = aux + Integer.parseInt((String) tabla.getValueAt(i, 3));
        }
        tot.setText(aux + "");
    }
